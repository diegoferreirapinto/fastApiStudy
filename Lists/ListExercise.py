zoo = ['dog', 'cat', 'cow', 'bird', 'elephant']  # create a list of 5 animals called zoo
print(zoo)

zoo.pop(2)  # delete the third position
print(zoo)

zoo.append('bat')  # append a new animal at the end of the list
print(zoo)

zoo.pop(0)  # delete the animal at the beginning of the list.
print(zoo)

print(zoo)  # print all the animals

print(zoo[0:3])  # print only the first 3 animals
